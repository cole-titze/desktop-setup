set -euo pipefail

echo "==> Setting up ENB lighting for Skyrim Special Edition (Bazzite / Linux)"
echo
echo "This step is entirely manual — ENB binaries, the weather mod, and the"
echo "preset must all be downloaded by hand. This script just prints the"
echo "steps in order. Run step 31 (skyrim-modding) first if you haven't already."
